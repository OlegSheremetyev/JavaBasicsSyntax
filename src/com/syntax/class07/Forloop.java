package com.syntax.class07;

public class Forloop {

	public static void main(String[] args) {
		// for loop
		
		
		//for (initialization;condition; increment/decrement0 {block of code}
		//Say hi 5 times 
		
		for (int i=1; i<=5; i++) {System.out.print("Good morning");} // 5 times
		// for (int i=1; i<=5; i--) {System.out.print("Hello");}// infinite
		for (int i=1; i>=5; i++) {System.out.print("How are you");}// 0 times
		
		
		
		
		
		
		
		
		
	}

}
