package com.syntax.class02;

public class WaysToCreateVariables {

	public static void main(String[] args) {
		/* 1 way to create a variable 
		 * DataType name = value
		 * Declare a variable and we initializing 
		 */
		int temperature=60;
		System.out.println(temperature);

		/* 2 way to create variable
 * Create a variable (dataType and name)
 * assign the value
 */
		int sum;//createe a variable
		sum=30;//assign the value
		System.out.println(sum);
		sum=50;//reassign value
		System.out.println(sum);
		int num1, num2, num3; //create 3 variables of int type
		num1=10;//assigning value
		num2=20;//assigning value
		num3=30;//assigning value
		System.out.println(num1);

	}

}
